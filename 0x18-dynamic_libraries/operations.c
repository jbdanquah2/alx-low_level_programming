#include "header.h"

/**
 * add - adds to two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: addition of two numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a subtraction of two numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a multiplication of two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return: dividend of two numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - the remainder of dividing two numbers
 * @a: first number
 * @b: second number
 *
 * Return: remainder of a div
 */
int mod(int a, int b)
{
	return (a % b);
}
